"""
Dataset for DeepSEA report experiments.

Reads .npy files generated by jakublipinski/build-deepsea-training-dataset.
Training set is accessed via numpy memmap (no full loading into RAM).
Val/test sets are loaded into memory (small enough).
"""
import random
from typing import Iterator, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader, IterableDataset, get_worker_info


NUM_TRAIN = 4400000
NUM_VAL = 8000


class DeepSEADataset(Dataset):
    """Map-style dataset using memmap — minimal RAM usage."""

    def __init__(self, data_path: str, labels_path: str, seq_len: int = 1000):
        self.data = np.load(data_path, mmap_mode='r')    # (N, 1000, 4)  uint8
        self.labels = np.load(labels_path, mmap_mode='r') # (N, 919)     uint8
        self.seq_len = seq_len
        self.crop_start = (1000 - seq_len) // 2 if seq_len < 1000 else 0

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        x = self.data[idx]                                          # (1000, 4)
        if self.crop_start > 0:
            x = x[self.crop_start:self.crop_start + self.seq_len]   # (seq_len, 4)
        x = torch.from_numpy(x.astype(np.float32)).T                # (4, seq_len)
        y = torch.from_numpy(self.labels[idx].astype(np.float32))   # (919,)
        return x, y


class TrainIterableDataset(IterableDataset):
    """Streaming dataset for training: memmap + shuffle per epoch."""

    def __init__(self, data_path: str, labels_path: str,
                 seq_len: int = 1000, shuffle_buffer: int = 5000):
        self.data_path = data_path
        self.labels_path = labels_path
        self.seq_len = seq_len
        self.buf_size = shuffle_buffer
        self._n = NUM_TRAIN

    def __iter__(self) -> Iterator[Tuple[torch.Tensor, torch.Tensor]]:
        wi = get_worker_info()

        # Memmap (re-open in each worker to avoid sharing issues)
        data = np.load(self.data_path, mmap_mode='r')
        labels = np.load(self.labels_path, mmap_mode='r')

        # Partition indices across workers
        if wi is None:
            indices = list(range(self._n))
        else:
            random.seed(torch.initial_seed() + wi.id)
            indices = [i for i in range(self._n) if i % wi.num_workers == wi.id]

        random.shuffle(indices)
        crop_start = (1000 - self.seq_len) // 2 if self.seq_len < 1000 else 0
        buffer = []

        for idx in indices:
            x = data[idx]                                               # (1000, 4)
            if crop_start > 0:
                x = x[crop_start:crop_start + self.seq_len]
            x = torch.from_numpy(x.astype(np.float32)).T               # (4, seq_len)
            y = torch.from_numpy(labels[idx].astype(np.float32))       # (919,)
            item = (x, y)

            if len(buffer) >= self.buf_size:
                buf_idx = random.randrange(self.buf_size)
                yield buffer[buf_idx]
                buffer[buf_idx] = item
            else:
                buffer.append(item)

        random.shuffle(buffer)
        for item in buffer:
            yield item


class DeepSEAIterableDataset(TrainIterableDataset):
    """Compat wrapper expected by training script."""

    def __init__(self, data_dir: str, seq_len: int = 1000, shuffle_buffer: int = 5000):
        super().__init__(
            f'{data_dir}/train_data.npy',
            f'{data_dir}/train_labels.npy',
            seq_len,
            shuffle_buffer,
        )


def create_dataloaders(data_dir: str = 'data', batch_size: int = 16,
                       seq_len: int = 1000, num_workers: int = 4,
                       pin_memory: bool = True, shuffle_buffer: int = 5000):
    """Create train/val/test dataloaders from .npy files."""

    train_ds = TrainIterableDataset(
        f'{data_dir}/train_data.npy',
        f'{data_dir}/train_labels.npy',
        seq_len, shuffle_buffer,
    )
    val_ds = DeepSEADataset(
        f'{data_dir}/valid_data.npy',
        f'{data_dir}/valid_labels.npy',
        seq_len,
    )
    test_ds = DeepSEADataset(
        f'{data_dir}/test_data.npy',
        f'{data_dir}/test_labels.npy',
        seq_len,
    )

    train_loader = DataLoader(train_ds, batch_size=batch_size,
                              num_workers=num_workers, pin_memory=pin_memory,
                              drop_last=True)
    val_loader = DataLoader(val_ds, batch_size=batch_size, shuffle=False,
                            num_workers=num_workers, pin_memory=pin_memory)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False,
                             num_workers=num_workers, pin_memory=pin_memory)
    return train_loader, val_loader, test_loader
